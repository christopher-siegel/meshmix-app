## Shell Script to download all plugins for the App
echo 'Add Crosswalk WebView plugin'
cordova plugins add cordova-plugin-crosswalk-webview

echo 'Add Crosswalk Whitelist plugin'
cordova plugins add cordova-plugin-whitelist

echo 'Add Crosswalk Cordova Native Audio plugin'
cordova plugins add de.neofonie.cordova.plugin.nativeaudio

echo 'Add Crosswalk File plugin'
cordova plugins add cordova-plugin-file

echo 'Add Crosswalk InAppBrowser plugin'
cordova plugins add cordova-plugin-inappbrowser

echo 'Add Crosswalk Media plugin'
cordova plugins add cordova-plugin-media

echo 'Add Crosswalk Splashscreen plugin'
cordova plugins add cordova-plugin-splashscreen

echo 'Add Crosswalk Text to speech plugin'
cordova plugins add cordova-plugin-tts
